# Palyginkite du skaičius

number_1 = float(input("Įveskite pirmą skaičių: "))
number_2 = float(input("Įveskite antrą skaičių: "))

if number_1 > number_2:
    print(f"Didesnis skaičius yra {number_1}")
elif number_1 == number_2:
    print(f"Abu skaičiai yra lygūs {number_1} = {number_2}")
else:
    print(f"Didesnis skaičius yra {number_2}") 
    