# operations with string data type

def to_uppercase(user_input: str) -> str:
    return user_input.upper()

def to_lowercase(user_input: str) -> str:
    return user_input.lower()

def reverse_string(user_input: str) -> str:
    return user_input[::-1]

def count_characters(user_input: str) -> int:
    return len(user_input)

